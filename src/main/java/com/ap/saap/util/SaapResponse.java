package com.ap.saap.util;

public class SaapResponse {
	private String message;

	public SaapResponse() {

	}

	public SaapResponse(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}

